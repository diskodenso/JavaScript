"use strict";

// logische "und" = &&
// logische "oder" = ||
// logische "nicht" = !

// Mit logischen Operatoren kann man versch. bedingungen aneinander 
// knöpfen und schauen ob sie wahr oder falsch sind

console.log(1 > 0 && 4 < 6); // console gibt true zurück

console.log(1 > 0 && 6 < 4); // console gibt false zurück, weil 6 nicht größer als 4 ist
// bei und muss beides richtig sein

console.log(1 < 0 || 6 > 4); // Console gibt true zurück

// bei "oder" || muss eines der beiden true sein

