"use strict";

// mit 0 weißen wir einen Wert zu bspl. let age = 24
// mit == testet man Werte auf gleichheit - loses gleichheit

// mit <, >, >=, <= testet man auf großer, kleiner, 
// größer gleich, kleiner gleich

console.log("Denis" == "Olf"); // false
console.log("Denis" != "Olf"); // true
console.log("Denis" > "Olf"); // false


// Jedes Zeichen hat einen Wert der voneinander Abgezogen wird. 
// Beispiel: Ein "K" hat einen kleineren Wert als ein "k"
// Es werden also die Werte Buchstabe für Buchstabe verglichen anhand der ASCI Tabelle

// mit === oder !== wird die strickte gleichheit bzw ungleichheit getestet
console.log("42" === 42); // false
console.log("42" !== 42); // true
